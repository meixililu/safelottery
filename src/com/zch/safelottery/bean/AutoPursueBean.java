package com.zch.safelottery.bean;

public class AutoPursueBean implements SafelotteryType{
	
	private String issue;
	
	private String mutiple;
	
	private String money;
	
	private String moneySum;
	
	private String profit;
	
	private String profitPercent;
	
	public String getIssue() {
		return issue;
	}
	public void setIssue(String issue) {
		this.issue = issue;
	}
	public String getMutiple() {
		return mutiple;
	}
	public void setMutiple(String mutiple) {
		this.mutiple = mutiple;
	}
	public String getMoney() {
		return money;
	}
	public void setMoney(String money) {
		this.money = money;
	}
	public String getMoneySum() {
		return moneySum;
	}
	public void setMoneySum(String moneySum) {
		this.moneySum = moneySum;
	}
	public String getProfit() {
		return profit;
	}
	public void setProfit(String profit) {
		this.profit = profit;
	}
	public String getProfitPercent() {
		return profitPercent;
	}
	public void setProfitPercent(String profitPercent) {
		this.profitPercent = profitPercent;
	}
	
	
}
